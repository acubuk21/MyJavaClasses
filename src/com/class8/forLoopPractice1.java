package com.class8;

public class forLoopPractice1 {

	public static void main(String[] args) {
	

		for (int a=1; a<=100; a++) {
			System.out.print(a + " ");
		}
		System.out.println (" ");
		System.out.println ("***");
		
		for (int a=100; a>=1; a--) {
			System.out.print(a + " ");
		}
		
		System.out.println (" ");
		System.out.println ("***");
		for (int a=2; a<=20; a+=2) {
			System.out.print(a + " ");
		}
		
		System.out.println (" ");
		System.out.println ("***");
		for (int a=21; a<=50; a+=2) {
			System.out.print(a + " ");
		}	
	}

}
